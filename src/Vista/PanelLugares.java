    public double cantCostoTo() {

        int cant1 = 0;

        for (int i = 0; i < al.tamaño(); i++) {
            PersonasTuristicas er = al.obtiene(i);
            if (er.totalaPagar() != 0) {
                cant1 += er.totalaPagar();
            }
        }
        return cant1;
    }

