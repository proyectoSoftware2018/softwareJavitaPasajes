    void lista(PersonasTuristicas p) {

        if (p instanceof PersonaTuristaNacional) {
            txtSalidaBoleta.append("-------------------- VIAJERO NACIONAL -----------------------" + "\n");
        }
        if (p instanceof PersonaTuristaUniversitario) {
            txtSalidaBoleta.append("-------------------- VIAJERO UNIVERSITARIO -----------------------" + "\n");
        }
        if (p instanceof PersonaTuristaExtranjero) {
            txtSalidaBoleta.append("-------------------- VIEAJERO EXTRANJERO -----------------------" + "\n");
        }

        txtSalidaBoleta.append(p.info());
        if (p instanceof PersonaTuristaNacional) {
            txtSalidaBoleta.append("Tipo de viajero\t:" + "Turista Nacional" + "\n");
            txtSalidaBoleta.append("Dato de turista Nacional\t:" + ((PersonaTuristaNacional) (p)).getDepNacimiento() + "\n");
            txtSalidaBoleta.append("Descuento\t:" + "\t" + "15%" + "\n" + "\n");

        } else if (p instanceof PersonaTuristaUniversitario) {
            txtSalidaBoleta.append("Tipo de viajero\t:" + "Turista Universitario" + "\n");
            txtSalidaBoleta.append("El operador es\t:" + ((PersonaTuristaUniversitario) (p)).getNumCarnet() + "\n");
            txtSalidaBoleta.append("Descuento\t:" + "\t" + "50%" + "\n" + "\n");
        } else {
            txtSalidaBoleta.append("Tipo de viajero\t:" + "Turista Extranjero" + "\n");
            txtSalidaBoleta.append("El operador es\t:" + ((PersonaTuristaExtranjero) (p)).getCarnetExtranjeria() + "\n");
            txtSalidaBoleta.append("Descuento\t:" + "\t" + "No tiene descuento" + "\n" + "\n");
        }
    }

