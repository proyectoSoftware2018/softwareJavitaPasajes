    public double costoTotal(int cant, String destino) {
        double precioFinal = 0.00;
        double precio;
        if (destino.equalsIgnoreCase("Amazonas")) {
            precio = 100.00;
            precioFinal = precio * cant;
            return precioFinal;
        }
        if (destino.equalsIgnoreCase("Apurimac")) {
            precio = 60.00;
            precioFinal = precio * cant;
            return precioFinal;
        }
        if (destino.equalsIgnoreCase("Lima")) {
            precio = 65.00;
            precioFinal = precio * cant;
            return precioFinal;
        }
        if (destino.equalsIgnoreCase("Ica")) {
            precio = 50.00;
            precioFinal = precio * cant;
            return precioFinal;
        }
        if (destino.equalsIgnoreCase("Puno")) {
            precio = 70.00;
            precioFinal = precio * cant;
            return precioFinal;
        }
        if (destino.equalsIgnoreCase("Cuzco")) {
            precio = 59.00;
            precioFinal = precio * cant;
            return precioFinal;
        }
        if (destino.equalsIgnoreCase("Arequipa")) {
            precio = 50.00;
            precioFinal = precio * cant;
            return precioFinal;
        }
        if (destino.equalsIgnoreCase("Ayacucho")) {
            precio = 100.00;
            precioFinal = precio * cant;
            return precioFinal;
        }
        if (destino.equalsIgnoreCase(txtNuevoDestino.getText())) {
            precio = Double.parseDouble(txtNuevoPrecio.getText());
            precioFinal = precio * cant;
            return precioFinal;

        }
        return precioFinal;
    }

